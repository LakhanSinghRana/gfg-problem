class Solution {
  public:
    int missingNum(vector<int>& arr) {
        // code here
        long long int n=arr.size()+1;
        long long int total=n*(n+1)/2;
        long long int asum=0;
        for(int i=0;i<n-1;i++){
            asum+=(long long)arr[i];
        }
        long long int miss=total-asum;
        return (int)miss;
    }
};


